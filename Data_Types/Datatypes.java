package Data_Types;

class Datatypes {

    public static void main(String[] args) {
        // Primitive DataTypes
        int a = 13131313;
        float b = 2.16443165464f;
        double c = 2.11111115642243164f;
        int d = 'A';
        boolean e = true;
        short f = 26465;
        long g = 264645145;
        byte h = -128;

        // Non-Primitive DataTypes
        String s = "Akshay";
        int[] arr = { 1, 2, 3, 4, 5 };

        for (int i = 0; i < 5; i++) {
            System.out.println(arr[i]);
        }

    }
}